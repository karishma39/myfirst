package com.example.StudentCouseExample.Student;

public class Course {

	int cid;
	String cname;
	
	public Course(int cid, String cname) {
		super();
		this.cid = cid;
		this.cname = cname;
	}
	public int getCid() {
		return cid;
	}
	public String getCname() {
		return cname;
	}
	
	
}
